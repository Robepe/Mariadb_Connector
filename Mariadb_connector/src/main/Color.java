package main;

public class Color {

    // RESET
    public static final String RESET = "\u001B[0m";

    // TEXT COLOR
    public static final String BLACK = "\u001B[30m";    // NEGRO
    public static final String RED = "\u001B[31m";      // ROJO
    public static final String GREEN = "\u001B[32m";    // VERDE
    public static final String YELLOW = "\u001B[33m";   // AMARILLO
    public static final String BLUE = "\u001B[34m";     // AZUL
    public static final String PURPLE = "\u001B[35m";   // MORADO
    public static final String CYAN = "\u001B[36m";     // CIAN
    public static final String WHITE = "\u001B[37m";    // BLANCO

    // BACKGROUND COLOR
    public static final String BLACK_BACKGROUND = "\u001B[40m";     // FONDO NEGRO
    public static final String RED_BACKGROUND = "\u001B[41m";       // FONDO ROJO
    public static final String GREEN_BACKGROUND = "\u001B[42m";     // FONDO VERDE
    public static final String YELLOW_BACKGROUND = "\u001B[43m";    // FONDO AMARILLO
    public static final String BLUE_BACKGROUND = "\u001B[44m";      // FONDO AZUL
    public static final String PURPLE_BACKGROUND = "\u001B[45m";    // FONDO MORADO
    public static final String CYAN_BACKGROUND = "\u001B[46m";      // FONDO CIAN
    public static final String WHITE_BACKGROUND = "\u001B[47m";     // FONDO BLANCO
}
